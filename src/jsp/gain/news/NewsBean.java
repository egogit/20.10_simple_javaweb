package jsp.gain.news;

public class NewsBean {
	
	private long appid;
	private String apptitle;
	private String appnewsurl;
	private String appnewsauthor;
	private long appnewsdate;
	public long getAppid() {
		return appid;
	}
	public void setAppid(long appid) {
		this.appid = appid;
	}
	public String getApptitle() {
		return apptitle;
	}
	public void setApptitle(String apptitle) {
		this.apptitle = apptitle;
	}
	public String getAppnewsurl() {
		return appnewsurl;
	}
	public void setAppnewsurl(String appnewsurl) {
		this.appnewsurl = appnewsurl;
	}
	public String getAppnewsauthor() {
		return appnewsauthor;
	}
	public void setAppnewsauthor(String appnewsauthor) {
		this.appnewsauthor = appnewsauthor;
	}
	public long getAppnewsdate() {
		return appnewsdate;
	}
	public void setAppnewsdate(long appnewsdate) {
		this.appnewsdate = appnewsdate;
	}
	
	

}
