package jsp.gain.board;

public class BoardBean {
	
	private int boardidx;
	private String boardusername;
	private String boardtitle;
	private String boardcontent;   
	
	
	
	private String boarddate;
	private String boarddel;
	private String boardhit;
	
	
	public int getBoardidx() {
		return boardidx;
	}
	public void setBoardidx(int boardidx) {
		this.boardidx = boardidx;
	}
	public String getBoardusername() {
		return boardusername;
	}
	public void setBoardusername(String boardusername) {
		this.boardusername = boardusername;
	}
	public String getBoardtitle() {
		return boardtitle;
	}
	public void setBoardtitle(String boardtitle) {
		this.boardtitle = boardtitle;
	}
	public String getBoardcontent() {
		return boardcontent;
	}
	public void setBoardcontent(String boardcontent) {
		this.boardcontent = boardcontent;
	}
	public String getBoarddate() {
		return boarddate;
	}
	public void setBoarddate(String boarddate) {
		this.boarddate = boarddate;
	}
	public String getBoarddel() {
		return boarddel;
	}
	public void setBoarddel(String boarddel) {
		this.boarddel = boarddel;
	}
	public String getBoardhit() {
		return boardhit;
	}
	public void setBoardhit(String boardhit) {
		this.boardhit = boardhit;
	}
	

}
